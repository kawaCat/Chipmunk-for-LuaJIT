--====================================================================
local ffi  = require("ffi")
local chip = require("chipmunk_ffi")
--====================================================================

-- test chipmunk vector vertex
--====================================================================
local vec = ffi.new("cpVect[1]")
vec[0].x =1
vec[0].y =1
--====================================================================
-- test 
local t  = chip._cpv(1,1)  -- create cpVect object
print ( t.x, t.y)          -- print 
--====================================================================
t = chip._cpvadd   (vec[0],t)        print ( t.x, t.y )
t = chip._cpvsub   (vec[0],t)        print ( t.x, t.y )
t = chip._cpvmult  (vec[0],0.5)      print ( t.x, t.y )
t = chip._cpvrotate(vec[0],t)        print ( t.x, t.y )
t = chip._cpvperp  (vec[0])          print ( t.x, t.y )
--====================================================================

--====================================================================
-- Result
--====================================================================
--  1     1
--  2     2
-- -1    -1
-- 0.5    0.5
--  0     1
-- -1     1
--====================================================================